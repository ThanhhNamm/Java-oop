package managetHotel;

public class Room {

    String type;

    Double price;

    public Room () {
        this.price = price;
        this.type = type;

    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public Double getPrice() {
        return price;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getType() {
        return type;
    }


}
